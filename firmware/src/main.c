static bool ADC0Exec = false;

// 116 us Conversion Time
// 86 samples in a 100 Hz
// Durata: 4.2us
#define MAXMIN_VAC_CYCLES (10 * 10000/AC_SMP_TIME_us)

static void ADC0ExecProcedure(void){
    static unsigned short  cycles = MAXMIN_VAC_CYCLES;
    static float parzial_max = 0;
    static float parzial_min = 1000;
    
        
    WDT_Clear();   
    
    // VAC Sampling channel
    // GEts the current VAC
    

    // Massimo e minimo della tensione di ingresso
    if(VAC > parzial_max){
        parzial_max = VAC;
    }

    if(VAC < parzial_min){
        parzial_min = VAC;
    }

    // Every
    cycles--;        
    if(cycles == 0){
       maxVAC =  parzial_max;
       minVAC =  parzial_min;
       if(minVAC > maxVAC) minVAC = maxVAC;
       parzial_max = 0;
       parzial_min = 1000;
       cycles = MAXMIN_VAC_CYCLES;
    }

    // Scrittura della corrente di riferimento     
    VOUT = (float) ADC1_ConversionResultGet() *  0.0106 ;/// 1.6;
    
    if(maxVAC < MIN_VACIN){
        alarm_VAC = true;
        alarm_vac_count = ALARM_VAC_TMO; // 15ms unit
    }

   

    #ifdef TEST_SWITCH
        DAC_DataWrite(DAC_CHANNEL_0,65535);
        return;
    #endif

    #ifdef TEST_REFERENCE
        ExecTestReference();
        return;
    #endif

    ExecControl(false);
    
    return;
}

static bool isVAC = true;
static void ADC0_Callback(ADC_STATUS status, uintptr_t context){
   
    if(!(status & ADC_STATUS_RESRDY)) return;
   
    if(isVAC){
        VAC = (float) ADC0_ConversionResultGet() * 375 / 65536;
        ADC0_ChannelSelect( ADC_POSINPUT_AIN10, ADC_NEGINPUT_GND); 
        isVAC= false;
        ADC0_ConversionStart();
      
    }else{
        IOUT = (float) ((((float) ADC0_ConversionResultGet() *4.95) / 65.535) - 2500)/100.0;
        IOUT = IOUT * 860/1200;
        if(IOUT<0) IOUT = 0;
        ADC0_ChannelSelect( ADC_POSINPUT_AIN5, ADC_NEGINPUT_GND); 
        isVAC= true;
        ADC0_ConversionStart();
        
    }
    ADC0Exec = true;
}

 
// Before to enter a running mode the following conditions shall be fullfilled
// - Precharge Activated
// - Power Contactor Activated
// - Min VAc max = 250V (180VAC) without Load

void InitializationLoop(void){
    
    uC_ENABLE_CONTACTOR_Set(); // Activates the Power Contactor
    mosfetActivation(false);
    alarm_overcurrent = false;
   
   
    uC_DISABLE_PRECHARGE_Set(); // Disable the precharge     
    ExecControl(true);
    TC0_TimerStart();
    return;
}

